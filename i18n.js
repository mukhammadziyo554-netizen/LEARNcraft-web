// Language Manager
const LearnCraftI18n = {
    currentLang: 'en',
    STORAGE_KEY: 'learncraft_language',
    
    init() {
        // Load saved language
        const saved = localStorage.getItem(this.STORAGE_KEY);
        if (saved && translations[saved]) {
            this.currentLang = saved;
        }
        
        // Set HTML lang attribute
        document.documentElement.lang = this.currentLang;
        
        console.log('ðŸŒ i18n initialized. Language:', this.currentLang);
    },
    
    setLanguage(lang) {
        if (!translations[lang]) {
            console.error('Language not supported:', lang);
            return false;
        }
        
        this.currentLang = lang;
        localStorage.setItem(this.STORAGE_KEY, lang);
        document.documentElement.lang = lang;
        
        console.log('ðŸŒ Language changed to:', lang);
        
        // Trigger custom event for language change
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
        
        return true;
    },
    
    getLanguage() {
        return this.currentLang;
    },
    
    t(key) {
        const keys = key.split('.');
        let value = translations[this.currentLang];
        
        for (const k of keys) {
            if (value && typeof value === 'object') {
                value = value[k];
            } else {
                // Fallback to English
                console.warn(`Translation missing for ${key} in ${this.currentLang}, using English`);
                value = translations.en;
                for (const fallbackKey of keys) {
                    if (value && typeof value === 'object') {
                        value = value[fallbackKey];
                    } else {
                        return key; // Return key if even English is missing
                    }
                }
                break;
            }
        }
        
        return value || key;
    },
    
    translatePage() {
        // Find all elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.t(key);
            
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
        });
        
        // Handle elements with data-i18n-html (for HTML content)
        document.querySelectorAll('[data-i18n-html]').forEach(element => {
            const key = element.getAttribute('data-i18n-html');
            element.innerHTML = this.t(key);
        });
        
        console.log('âœ… Page translated to:', this.currentLang);
    }
};

// Initialize on load
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => LearnCraftI18n.init());
} else {
    LearnCraftI18n.init();
}

// Export for global use
window.LearnCraftI18n = LearnCraftI18n;
